/**
 * Enumeration to handle the move generation in n/e/s/w directions
 */
export enum Direction {
    Up,
    Down,
    Left,
    Right
}

/**
 * Enumeration to handle the move generation in diagonal directions
 */
export enum DirectionAngle {
    UpRight,
    UpLeft,
    DownRight,
    DownLeft
}
